package cn.edu.hnnu.chap02.sec03;

public class Demo1 {

	public static void main(String args[]){
		// 定义一个int类型的变量a
		int a;
		// 给变量a赋值
		a = 1;
		System.out.println(a);
		
		// 定义一个int类型的变量a2
		int a2 = 2;
		System.out.println("a2="+a2);
		
		// 定义一个byte类型的变量b
		byte b = 3;
		System.out.println("b="+b);
		
		// 定义一个short类型的变量s
		short s = 4;
		System.out.println("s="+s);
		
		// 定义一个long类型的变量l
		long l = 5;
		System.out.println("l="+l);
		
		int a11 = 1;
		int a22 = 2;
		// 简单的一个表示式计算
		int a3=a11+a22;
		System.out.println("a1+a2="+a3);
	}
}
